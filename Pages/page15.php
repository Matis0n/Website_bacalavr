<h1>Ультразвуковая дефектоскопия фазированными решетками</h1>

<p >Контроль методом TOFD
Метод, основанный на использовании двух датчиков, работающих в раздельном режиме. В этом методе обрабатываются сигналы, дифрагированные от границ дефекта, при этом происходит точное определение, как местоположения, так и размеров дефекта.
Данные TOFD отображаются в виде B-скана с серой полутоновой шкалой. TOFD обеспечивает контроль практически всего объема шва и определение размеров дефектов независимо от амплитуды сигналов в соответствии с стандартом ASME-2235.
Сканирование вдоль одной линии с контролем всего объема шва
Параметры настройки не зависят от формы шва
Высокая чувствительность ко всем видам дефектов и нечувствительность к их ориентации.</p>

