<p>Данный метод предлагает следующие возможности:</p>
<ul class="ulli">
<li>Программное управление углом наклона луча, фокальным расстоянием, и размером самого фокуса.</li>
<li>Дефектоскопия с множеством углов ввода, с использованием одного небольшого программно управляемого, многоэлементного датчика.</li>
<li>Большие возможности, для контроля изделий сложной формы.</li>
<li>Высокоскоростной контроль, не требующий перемещения объекта или датчика.</li>
</ul>
