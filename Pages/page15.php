<p >Технология фазированных решеток основывается на генерации ультразвукового луча и диаграммы приема УЗ сигнала, с возможным изменением его параметров, таких как угол наклона, фокальное расстояние, размер фокуса, с помощью программного обеспечения. Кроме того, зона, генерирующая этот луч, может мультиплексироваться вдоль длинной решетки. Эти характеристики открывают ряд новых возможностей в области дефектоскопии. Например, теперь существует возможность быстрого изменения угла ввода луча для проведения контроля детали без изменения положения самого датчика.
Фазированные решетки также подразумевают замену многоэлементных датчиков и даже механических деталей.
Дефектоскопия детали с изменением угла ввода дает максимальный эффект независимо от ориентации дефекта, при оптимальном соотношении сигнал/шум.</p>


